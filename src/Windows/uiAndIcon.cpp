GdkPixbuf* uiAndIcon::GetIconPixBuf(){
  string raw = Base64::fromBase64(uiAndIcon::iconBase64);
  GBytes* bytes = g_bytes_new((gconstpointer)raw.data(), raw.size());
  GInputStream* stream = g_memory_input_stream_new_from_bytes(bytes);
  GError* error = NULL;
  GdkPixbuf* buf = gdk_pixbuf_new_from_stream(stream, NULL, &error);
  if (error)
    cout << error->message << endl;
  return buf;
}

